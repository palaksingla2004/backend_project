const mongoose = require('mongoose');

// Define Product Schema
const ProductSchema = new mongoose.Schema({
  id: {
    type: Number,
    required: true,
    unique: true
  },
  name: {
    type: String,
    required: true
  },
  description: {
    type: String,
    required: true
  },
  image: {
    type: String,
    required: true
  },
  category: {
    type: String,
    required: true
  },
  new_price: {
    type: Number,
    required: true
  },
  old_price: {
    type: Number,
    required: true
  },
}, { timestamps: true });

// Create a Product model
const Product = mongoose.model('Product', ProductSchema);

module.exports = Product;
